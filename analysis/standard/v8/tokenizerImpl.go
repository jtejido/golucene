func zzUnpackTrans(packed []int) []int {
	m := make([]int, 1650)
	j := 0
	for i, l := 0, len(packed); i < l; i += 2 {
		count, value := packed[i], packed[i+1]-1
		m[j] = value
		j++
		count--
		for count > 0 {
			m[j] = value
			j++
			count--
		}
	}
	return m
}

